drop table user if exists
drop table tour if exists

create table user (id bigint, name varchar(100), password varchar(100), user_type varchar(2))

create table tour (id bigint generated by default as identity (start with 1), status varchar(2), user_id bigint, mode_travel varchar(40), ticket_cost bigint, cab_h bigint, cab_d bigint, hotel_cost bigint, start_date date, end_date date, description varchar(300), tobe_approvedby bigint, approvedat timestamp, rejectedat timestamp, primary key(id))